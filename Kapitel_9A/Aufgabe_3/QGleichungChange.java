public class QGleichungChange {
    public static void main(String[] args) {

        double x;
        double result;

        x = 4.0;
        result = 3 * x * x - 8 * x + 4;
        System.out.println(result);
        
        x = 2.0;
        result = 3 * x * x - 8 * x + 4;
        System.out.println(result);

        x = 2.0/3.0;
        result = 3 * x * x - 8 * x + 4;
        System.out.println(result);
    }
}
